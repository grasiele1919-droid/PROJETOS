dia_semana = (int(input("Digite o número do dia da semana: ")))

if dia_semana == 1:
    print("Segunda-feira, dia útil.")
elif dia_semana == 2:
    print("Terça-feira, dia útil.")
elif dia_semana == 3:
    print("Quarta-feira, dia útil.")
elif dia_semana == 4:
    print("Quinta-feira, dia útil.")
elif dia_semana == 5:
    print("Sexta-feira, dia útil.")
elif dia_semana == 6:
    print("Sábado-feira, final de semana.")
elif dia_semana == 7:
    print("Domingo-feira, final de semana.")
else:
    print("Numero digitado não é válido.")