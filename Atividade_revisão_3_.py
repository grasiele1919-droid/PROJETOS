numero = int(input ("Digite um número: "))
# numero recebe entrada inteiro.

if numero < 0:
# SE o numero for menor 0 = negativo
    print(f"Número {numero}, é negativo")

elif numero >0:
#SENÂO o numero for maior que 0= positivo
    print(f"Número {numero}, é positivo.")

else:
    print ("É zero.")
          
